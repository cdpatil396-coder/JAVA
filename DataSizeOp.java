class DataSizeOp{
	public static void main(String args[]){
	System.out.println("byte: " +Byte.BYTES+" byte");  //1 byte
	System.out.println("short: " +Short.BYTES+" byte");//2 bytes
	System.out.println("Integer: " +Integer.BYTES+" byte");//4 bytes
	System.out.println("Long: " +Long.BYTES+" byte"); //8 byte s
	System.out.println("Float: " +Float.BYTES+" byte"); //4 bytes
	System.out.println("Double: " +Double.BYTES+" byte"); //8 bytes
	System.out.println("Character: " +Character.BYTES+" byte"); //2 bytes

	}
}